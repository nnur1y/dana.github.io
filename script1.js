$(document).ready(function(){
    $(".one1").hover(function () {
        $(this).prev().css("box shadow", "0 4px 8px 0 rgba(0, 0, 0, 0.2), 0 6px 20px 0 rgba(0, 0, 0, 0.19)");
    }, function (){
        $(this).prev().css("box shadow", "");
    });
}
);

$(document).ready(function(){
    $('.one1').hover(
        function(){
            $(this).animate({
                marginTop: "-=2%",
            },200);
        },
        function(){
            $(this).animate({
                marginTop: "0%"
            },200);
        }
    )
    
});


